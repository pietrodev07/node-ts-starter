export const math = {
  add: (a: number, b: number) => a + b,
  subtract: (a: number, b: number) => a - b,
  multiply: (a: number, b: number) => a * b,
  divide: (a: number, b: number) => a / b,
  power: (a: number, b: number) => a ** b,
  sqrt: (a: number) => a ** 0.5,
  square: (a: number) => a ** 2,
  cube: (a: number) => a ** 3,
};
